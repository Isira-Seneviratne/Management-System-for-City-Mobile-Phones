    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        try
        {
            if(!txtDisplay.getText().matches("^[0-9]{1,}+%$"))
                num2 = Double.parseDouble(txtDisplay.getText());
            else
            {
                String s = txtDisplay.getText();
                num2 = Double.parseDouble(s.substring(0, s.indexOf("%"))) / 100;
            }
            switch(op)
            {
                case '+':   result = num1 + num2;
                            break;

                case '-':   result = num1 - num2;
                            break;

                case '*':   result = num1 * num2;
                            break;

                case '/':   result = num1 / num2;
                            break;
            }
            txtDisplay.setText(Double.toString(result).replaceAll("\\.0*$", ""));
        }
        catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(this, "Enter a valid number.", "Error", JOptionPane.ERROR_MESSAGE);
            txtDisplay.setText("");
        }
    }//GEN-LAST:event_jButton13ActionPerformed

