    public void calculate(String o)
    {
        try
        {
            if(!txtDisplay.getText().matches("^[0-9]{1,}+%$"))
                num1 = Double.parseDouble(txtDisplay.getText());
            else
            {
                String s = txtDisplay.getText();
                num1 = Double.parseDouble(s.substring(0, s.indexOf("%"))) / 100;
            }
            op = o.charAt(0);
            txtDisplay.setText(o);
        }
        catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(this, "Enter a valid number.", "Error", JOptionPane.ERROR_MESSAGE);
            txtDisplay.setText("");
        }
    }
    
