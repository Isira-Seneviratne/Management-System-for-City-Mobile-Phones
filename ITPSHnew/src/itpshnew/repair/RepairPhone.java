    private void cmbBrandActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbBrandActionPerformed
        String a = cmbBrand.getSelectedItem().toString();
        int xo = cmbitem.getItemCount(); //reset Brand ComboShip
        for(int i = 0; i<xo; i++)
        cmbitem.removeItemAt(0);
        try
        {
            //load Brands to comboBrand
            String abc = "Select itemName from item where brand = '"+ a +"' group by itemName order by itemName";
            pst = (com.mysql.jdbc.PreparedStatement) conn.prepareStatement(abc);
            ResultSet rs = pst.executeQuery(abc);
            while (rs.next()) {
                cmbitem.addItem(rs.getString("itemName"));
            }
            cmbitem.setSelectedItem(1);

        } catch (SQLException ex) {}
    }//GEN-LAST:event_cmbBrandActionPerformed

