    private void dbConnect()
    {
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/ManagementSystem?verifyServerCertificate=false&useSSL=true", "root", "abcd1234");
        }
        catch(ClassNotFoundException ce)
        {
            JOptionPane.showMessageDialog(this, "Unable to load the database driver.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        catch(SQLException se)
        {
            JOptionPane.showMessageDialog(this, "Unable to connect to the database.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void calculateResults()
    {
        if(conn == null)
            dbConnect();
        try
        {
            Statement s = conn.createStatement();
        }
        catch(NullPointerException npe)
        {
            JOptionPane.showMessageDialog(this, "A database connection was not properly established.",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
        catch(SQLException se)
        {
            JOptionPane.showMessageDialog(this, "There was an error while retrieving financial values to calculate results.",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
        try
        {
            conn.close();
        }
        catch(SQLException se)
        {
            JOptionPane.showMessageDialog(this, "Unable to close database connection.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void updateResults()
    {
        try
        {
            RepRev = Float.parseFloat(repair_rev.getText());
            RepCost = Float.parseFloat(repair_cost.getText());
            RepProf = RepRev - RepCost;
            repair_prof.setText(Float.toString(RepProf).replaceAll("\\.0*$", ""));
            
            SalesRev = Float.parseFloat(sales_rev.getText());
            SalesCost = Float.parseFloat(sales_cost.getText());
            SalesProf = SalesRev - SalesCost;
            sales_prof.setText(Float.toString(SalesProf).replaceAll("\\.0*$", ""));
            
            DisRev = Float.parseFloat(dis_rev.getText());
            DisCost = Float.parseFloat(dis_cost.getText());
            DisProf = DisRev - DisCost;
            dis_prof.setText(Float.toString(DisProf).replaceAll("\\.0*$", ""));
            
            OrdRev = Float.parseFloat(ord_rev.getText());
            OrdCost = Float.parseFloat(ord_cost.getText());
            OrdProf = OrdRev - OrdCost;
            ord_prof.setText(Float.toString(OrdProf).replaceAll("\\.0*$", ""));
            
            OtherCost = Float.parseFloat(other_costs.getText());
            
            TotRev = RepRev + SalesRev + DisRev + OrdRev;
            TotCost = RepCost + SalesCost + DisCost + OrdCost + OtherCost;
            TotProf = TotRev - TotCost;
            tot_rev.setText(Float.toString(TotRev).replaceAll("\\.0*$", ""));
            tot_cost.setText(Float.toString(TotCost).replaceAll("\\.0*$", ""));
            tot_prof.setText(Float.toString(TotProf).replaceAll("\\.0*$", ""));

            JOptionPane.showMessageDialog(this, "The financial values have been successfully updated.",
                    "Financial values updated", JOptionPane.INFORMATION_MESSAGE);
        }
        catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(this, "You have attempted to store blank or invalid numbers. Please enter valid numbers.",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void generateReport()
    {
        if(conn == null)
            dbConnect();
        if(TotProf == 0 && TotRev == 0 && TotCost == 0)
        {
            JOptionPane.showMessageDialog(this, "You have not generated the required financial values. Please click the \"Calculate Results\" button to do so.",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String month = getMonth();
        int year = Calendar.getInstance().get(Calendar.YEAR);
        try
        {
            Statement s = conn.createStatement();
            String title, message;
            s.execute("SELECT * FROM Financial_Reports WHERE Month_issued='"+month+"' AND Year_issued="+year);
            if(s.getResultSet().next())
            {
                s.execute("UPDATE Financial_Reports SET Total_revenue="+TotRev+", Total_costs="+TotCost+", Total_profit="+TotProf
                        +" WHERE Month_issued='"+month+"' AND Year_issued="+year);
                title = "Update successful";
                message = "The financial report for the current month and year has been updated.";
            }
            else
            {
                s.execute("INSERT INTO Financial_Reports VALUES('"+month+"',"+year+","+TotRev+","+TotCost+","+TotProf+")");
                title = "Insertion successful";
                message = "The financial report for the current month and year has been inserted.";
            }
            JOptionPane.showMessageDialog(this, message, title, JOptionPane.INFORMATION_MESSAGE);
        }
        catch(NullPointerException npe)
        {
            JOptionPane.showMessageDialog(this, "A database connection was not properly established.",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
        catch(SQLException se)
        {
            JOptionPane.showMessageDialog(this, "An error occurred while writing the financial report to the database.",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
        try
        {
            conn.close();
        }
        catch(SQLException se)
        {
            JOptionPane.showMessageDialog(this, "Unable to close database connection.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
